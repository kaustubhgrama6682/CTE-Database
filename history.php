<h1>The Early Years of Bothell High School
</h1>

<div class="container"> <!-- Added container -->

<p>The first school to provide high school classes was built in 1907 on the Hillcrest site, replacing the previous “white school on the hill” which served grades 1-8. The new structure, built to house grades 1-12 with its boxy, columned style, resembled many of the traditional schools being built in the Seattle at the time. The two large rooms on the main floor housed four grades each, while the upper floor was for the high school students. Professor Henry Simonds, who had moved to Bothell from the Midwest with his family in the early 1900s, had urged the community to provide high school classes here instead of sending students to Broadway High School in Seattle. He became the first high school principal. Once classes were underway in the handsome, new building, various rural districts around Bothell began contracting with District #46 to send their high school students to Bothell via small buses, even using milk trucks with benches!</p>

        <p>The first Bothell High School graduates emerged in 1912, a class of five consisting of Merle and Erma Olin, Nina Beckstrom, Carrie Ross, and Sarah Simonds. By 1921, Bothell was the educational center for what is now the Northshore School District. The 1907 school on the hill was now bursting at the seams. The total Bothell district enrollment was 330, 89 of whom were high school students.</p>

        <p>The school board could see a separate high school would be needed, so it purchased a site alongside the Bothell-Everett highway for $8,000 in 1921. The following year, the board asked voters for the district’s first special assessment, an $8,900 operating levy to cover the difference between what the state and county allocated and what the district actually needed.</p>

        <p>In September 1921, the school district removed an old orchard from a portion of the newly purchased site and created a football field. In honor of the occasion, “the high school football team was advanced $233 to buy new football suits,” says one entry in the 1921 school board minutes. A 1920 BHS graduate, Harold “Pop” Keeney, had been hired as the new coach and would subsequently take his team to the state tournament in 1923, losing to Eatonville 10-3 in the finals.</p>

        <p>In a May 1922 election, Bothell voters approved a $31,000 bond issue to build a new high school. The vote was 143-40, a 78% yes vote. The two-story brick structure that opened in September 1923, alongside the Bothell- Everett highway served the community for the next 30 years. When it opened in 1923, there were five teachers plus Coach Pop Keeney. W.D. Bay was the high school principal as well as the district superintendent, but he was so controversial, the school board paid him off in April 1924 and recruited E.J. McNamara from the North Bend School District. McNamara was promised a salary of $3,000 for the 1924-25 school year.</p>

        <p>By 1927-28, the four-year high school boasted an enrollment of 180. The duties of principal and superintendent were separated in 1929, with Martin Baker named high school principal and McNamara continuing as superintendent. By 1946, the high school had produced its first Rhodes Scholar.</p>

        <p>The current Bothell High School was constructed in 1953 on West Hill. Additions and renovations to the one- story, campus-style building have served the high school community well in the ensuing years. A new gymnasium was added in 1956 at a cost of $463,525. The 1923 structure on Bothell-Everett highway was not demolished until 1961 because it was used as an overflow structure when Bothell experienced an enrollment bulge in the late 1950s. The Ruiz-Costie County pool stands on the former high school site.</p>

